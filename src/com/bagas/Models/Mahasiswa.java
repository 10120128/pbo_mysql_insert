package com.bagas.Models;

public class Mahasiswa {
    private String nama;
    private String nim;
    private String alamat;
    private int nilai;

    public Mahasiswa(String nama, String nim, String alamat, int nilai) {
        this.nama = nama;
        this.nim = nim;
        this.alamat = alamat;
        this.nilai = nilai;
    }
}
